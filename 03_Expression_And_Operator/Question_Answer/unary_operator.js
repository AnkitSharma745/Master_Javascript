let a = 5;

// Unary Plus (+)
// Converts a value to a number (if it's not already a number)
console.log(+a); // 5: a is already a number

// Unary Negation (-)
// Converts a value to a number and negates it
console.log(-a); // -5: Negates the value of a

// Typeof Operator
// Returns the type of a value
console.log(typeof a); // 'number': a is a number
console.log(typeof "hello"); // 'string': 'hello' is a string

// NOT Operator (!)
// Returns the opposite boolean value
console.log(!a); // false: a (5) is truthy, so NOT a is false
