let x = 10;
let y = 3;

// Addition
console.log(x + y); // 13: Adds x and y

// Subtraction
console.log(x - y); // 7: Subtracts y from x

// Multiplication
console.log(x * y); // 30: Multiplies x and y

// Division
console.log(x / y); // 3.3333...: Divides x by y, returns quotient

// Modulus (Remainder)
console.log(x % y); // 1: Returns the remainder when x is divided by y

// Exponentiation
console.log(x ** y); // 1000: Raises x to the power of y (10^3)
