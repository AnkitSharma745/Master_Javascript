let a = 10;

// Addition assignment
a += 5; // a = a + 5
console.log(a); // 15

// Multiplication assignment
a *= 2; // a = a * 2
console.log(a); // 30

// Division assignment
a /= 3; // a = a / 3
console.log(a); // 10

// Subtraction assignment
a -= 4; // a = a - 4
console.log(a); // 6

// comma operator
let result = (1, 2, 3);

// The comma operator evaluates each expression from left to right
// and returns the value of the last expression
console.log(result); // 3: 1 and 2 are ignored, only 3 is returned
