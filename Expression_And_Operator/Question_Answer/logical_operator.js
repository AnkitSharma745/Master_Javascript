let x = true;
let y = false;

// Logical AND (&&)
// Returns true if both operands are true
console.log(x && y); // false: x is true, but y is false

// Logical OR (||)
// Returns true if at least one operand is true
console.log(x || y); // true: x is true, so the result is true

// Logical NOT (!)
// Returns the opposite of the operand's boolean value
console.log(!x); // false: x is true, so NOT x is false
